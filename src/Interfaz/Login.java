    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Extrae la informacion de los cuadros de texto y luego la verifica
        if (jTextField1.getText().isEmpty() | jPasswordField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No deje espacios sin rellenar");
        } else {
            String NombreUsuario = jTextField1.getText();
            String Contrase単a = jPasswordField1.getText();

            //Admin
            login.metodos.RegistrarAdministrador("a", "a", 22, "Admin", "admin", "Admin");
                
            // El metodo "InicioSesion" retorna un objeto tipo usuario, y la vartiable usuario almacena la informacion.
            Usuarios usuarios = login.metodos.InicioSesion(NombreUsuario, Contrase単a);

            // El metodo "InicioSesionAdmin" retorna un objeto tipo admin, y la vartiable admin almacena la informacion.
            Admin admin = login.metodos.InicioSesionAdmin(NombreUsuario, Contrase単a);

            if (usuarios != null) {
                RespaldoUsuario = usuarios;

                if (RespaldoUsuario.getTipoUsuario().equals("Corriente")) {
                    login.menuUsuarios.setVisible(true);
                    this.dispose();
                    UsuarioInfo.setText(login.RespaldoUsuario.getNombreUsuario());
                    jTextField1.setText(null);
                    jPasswordField1.setText(null);

                } else {

                    JOptionPane.showMessageDialog(null, "Usuario no exite");
                    jTextField1.setText(null);
                    jPasswordField1.setText(null);

                }

            } else {
                if (admin != null) {
                    RespaldoAdmin = admin;
                    if (RespaldoAdmin.getTipoUsuario().equals("Admin")) {
                        login.menuAdmin.setVisible(true);
                        this.dispose();
                        AdminInfo.setText(login.RespaldoAdmin.getNombreUsuario());
                        jTextField1.setText(null);
                        jPasswordField1.setText(null);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Usuario no exite");
                    jTextField1.setText(null);
                    jPasswordField1.setText(null);
                }

            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

