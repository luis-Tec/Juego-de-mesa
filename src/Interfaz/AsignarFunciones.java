    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (Login.RespaldoAdmin.getSigFunc() == null) {
            JOptionPane.showMessageDialog(null, "No hay funciones");
            return;
        }
        if (FuncionesAux == null) {
            FuncionesAux = Login.RespaldoAdmin.getSigFunc();
            listModel.clear();
            listModel.addElement(" Color: " + FuncionesAux.getColor());
            listModel.addElement(" Adelantar: " + FuncionesAux.getAdelantar());
            listModel.addElement(" Retrasar: " + FuncionesAux.getRetrasar());
            listModel.addElement(" Lanzar dado: " + FuncionesAux.getLanzarDado());
            jList1.setModel(listModel);
            return;
        }
        if (FuncionesAux.getSig() == null) {
            JOptionPane.showMessageDialog(null, "No hay funciones siguientes");
            return;
        }
        FuncionesAux = FuncionesAux.getSig();
        listModel.clear();
        listModel.addElement(" Color: " + FuncionesAux.getColor());
        listModel.addElement(" Adelantar: " + FuncionesAux.getAdelantar());
        listModel.addElement(" Retrasar: " + FuncionesAux.getRetrasar());
        listModel.addElement(" Lanzar dado: " + FuncionesAux.getLanzarDado());
        jList1.setModel(listModel);
    }//GEN-LAST:event_jButton4ActionPerformed

