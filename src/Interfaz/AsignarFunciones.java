    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (FuncionesAux == null) {
            JOptionPane.showMessageDialog(null, "No hay ninguna funcion seleccionada");
            return;
        }
        Pintor.CasillasTemporal.setColor(FuncionesAux.getColor());
        Pintor.CasillasTemporal.setAdelantar(FuncionesAux.getAdelantar());
        Pintor.CasillasTemporal.setRetrasar(FuncionesAux.getRetrasar());
        Pintor.CasillasTemporal.setLanzarDado(FuncionesAux.getLanzarDado());
        Pintor.CasillasTemporal = null;
        Login.mapaNuevo.jFrame.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

